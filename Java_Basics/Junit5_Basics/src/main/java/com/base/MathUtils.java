package com.base;

public class MathUtils {
	
	public int add(int a1, int a2) {
		return a1+a2;
	}
	
	public int subtract(int a1, int a2) {
		return a1-a2;
	}
	
	public int multiply(int a1, int a2) {
		return a1*a2;
	}
	public int divide(int a1, int a2) {
		return a1/a2;
	}
	
	public double area(int r) {
		return Math.PI*r*r;
	}
}
